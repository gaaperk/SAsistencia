      DefaultTableModel m = new DefaultTableModel();
        m=(DefaultTableModel)TabRegA.getModel();
        String clv,fecha,estado;
        int Clave;
        String HoraES []= new String [2];
        String HoraR []= new String [2];
        if(m.getRowCount()!=0){
            anuncio("",false);  
            if(bandera!=1){
                anuncio("",false); 
                InsertD();
                Motor mr = new Motor();
                for(int i=0;i<m.getRowCount();i++){
                    clv=m.getValueAt(i,0).toString();
                    Clave=Integer.parseInt(m.getValueAt(i,0).toString());
                    fecha=m.getValueAt(i,1).toString();
                    estado=m.getValueAt(i,4).toString();                   
                    if(Dif(i,clv,fecha)==false && estado.equals("Cargado")){                        
                       if(NoIgual(i,fecha,1)!=true) {
                         mr.FalT(fecha); // parametro fecha
                       }
                      try {
                            HoraES=mr.getHoraES(Clave,fecha); // clave y fecha -> Hora de entra y salida
                            HoraR=mr.getHoraReg(Clave,fecha); // clave y fecha -> Hora en la que registro Entrada y Salida
                            //JOptionPane.showMessageDialog(null,HoraES[0]+" // "+HoraES[1]);
                             //JOptionPane.showMessageDialog(null,HoraR[0]+" // "+HoraR[1]);
                            if(HoraR[1].equals("false")!=true){
                                mr.SalInc(HoraR[1],HoraES[1]+":00",Clave,fecha);
                            }
                            mr.EnInc(HoraES[0]+":00",Clave,HoraR[0],fecha);
                            
                        } catch (ParseException ex) {
                            Logger.getLogger(UpLoad.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    } 
                    
                }
               
            }else{anuncio("Archivo no compatible",true);  }
                
     }else{ anuncio("No ha cargo ningun registor en la tabla",true);  }
        
        
        
