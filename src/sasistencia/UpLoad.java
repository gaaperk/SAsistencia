    public void LeerTxt(String dir){
        DefaultTableModel m = new DefaultTableModel();
        m=(DefaultTableModel)TabRegA.getModel();
        Date format = new Date();
        int contS=0, contN=0;
        String fila[]=new String[5];
        try{
            BufferedReader bf= new BufferedReader(new FileReader(dir));
            String aux="";
            while((aux=bf.readLine())!=null){
                contS++;
               if(noDup(aux.substring(0,6),FormatoFecha(aux.substring(6,14)),FormatoHora(aux.substring(14,20)))==true){
                   contN++;
                continue;
            }else{
              fila[0]=aux.substring(0,6);
              fila[1]=FormatoFecha(aux.substring(6,14));
              fila[2]=FormatoHora(aux.substring(14,20));
              fila[3]=aux.substring(20);
              fila[4]="";
               m.addRow(fila);               
            }
            }            
            txtException.setText("Archivo cargado");
            bandera=0;
        }catch(Exception e){
          txtException.setText("Error al cargar archivo o formato no compatible");
          bandera=1;
        // JOptionPane.showMessageDialog(null,"bbb:"+ e);
        }
        TxtRegC.setText(""+contS);           
        //contN=contN+Integer.parseInt(TxtRegX.getText());
        TxtRegX.setText(""+contN);
    }
